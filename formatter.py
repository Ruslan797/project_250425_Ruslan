# Цвета ANSI для оформления текста в консоли

# Сброс цвета
RESET = "\033[0m"

# Основные цвета
GREEN = "\033[92m"
MAGENTA = "\033[95m"
RED = "\033[91m"
YELLOW = "\033[93m"
CYAN = "\033[96m"
BLUE = "\033[94m"

# Кастомный цвет (например, для ошибок или предупреждений)
BERUSA = "\033[38;5;208m"  # Оранжевый оттенок